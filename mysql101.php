  <H2>Database</H2>
  <ul>
  	<li>A set of tables</li>
  	<li>Access permissions are granted at the database level</li>
  </ul>
  <h2>Table</h2>
  <ul>
  	<li>A set of columns and rows</li>
  	<li>Your table should represent a single concept (a noun)</li>
  	<li>E.g: products, customers, orders, registration</li>
  </ul>
  <h2>Column</h2>
  <ul>
  	<li>A set of data of a single simple type</li>
  	<li>E.g: first_name, email, password</li>
  </ul>
  <h2>Row</h2>
  <ul>
  	<li>Single record of data</li>
  	<li>eg: first_name : james</li>
  </ul>
  <h2>Field</h2>
  <ul>
  	<li>An intersection of a column and a row</li>
  </ul>
  <h2>Index</h2>
  <ul>
  	<li>data structure used to increase lookup speed</li>
  </ul>
  <h2>Foreign Key</h2>
  <ul>
  	<li>A table column who's values reference rose in another table</li>
  	<li>The foundation of relational databases</li>
  </ul>
